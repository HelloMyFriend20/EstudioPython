def sumar(num1,num2):
    print(f'El resultado de la suma es: {num1+num2}')

def restar(num1,num2):
    print(f'El resultado de la resta es: {num1-num2}')

def multiplicar(num1, num2):
    print(f'El resultado de la multiplicación es: {num1*num2}')